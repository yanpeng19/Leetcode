int un(vector<vector<int>>& obstacleGrid)
{

	int m = obstacleGrid.size();
	int n = obstacleGrid[0].size();

	if (obstacleGrid[m - 1][n - 1] == 1) return 0;
	if (m == 1)
	{
		auto p = find(obstacleGrid[0].begin(), obstacleGrid[0].end(), 1);
		if (p == obstacleGrid[0].end()) return 1;
		else return 0;
	}
	if (n == 1)
	{
		for (auto a : obstacleGrid)
			if (a[0] == 1) return 0;
		return 1;
	}

	vector<vector<int>> table(m, vector<int>(n, 0));

	int flag = 0;
	for (int i = n-1; i >= 0; i--)
	{
		if (obstacleGrid[m - 1][i] != 1&&flag ==0)
			table[m - 1][i] = 1;
		else flag = 1;
	}
	for (auto a : table)
	{
		for (auto b : a)
			cout << b << " ";
		cout << endl;
	}

		
	flag = 0;
	for (int i = m-1; i >= 0; i--)
	{
		if (obstacleGrid[i][n - 1] != 1 && flag == 0)
			table[i][n - 1] = 1;
		else flag = 1;
	}

	for (auto a : table)
	{
		for (auto b : a)
			cout << b << " ";
		cout << endl;
	}
		

	for (int i = m - 2; i >= 0; i--)
		for (int j = n - 2; j >= 0; j--)
		{
			if(obstacleGrid[i][j]!=1)
				table[i][j] = table[i + 1][j] + table[i][j + 1];
		}
			

	return table[0][0];
}

